public class big2 {
    public static void main(String[] args) {
        int a = 7;       // Change to any value
        int b = 89;      // Change to any value

        if (a > b) {
            System.out.println(a + " is bigger.");
        } else if (b > a) {
            System.out.println(b + " is bigger.");
        } else {
            System.out.println("Both numbers are equal.");
        }
    }
}
